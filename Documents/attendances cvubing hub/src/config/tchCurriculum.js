// Helper functions for working with curriculum data
export const getCurriculumWeek = (weekNumber) => {
  return TCH_CURRICULUM_DATA.weeks.find(week => week.week === weekNumber)
}

export const getCharacterMetrics = (weekNumber) => {
  return TCH_CURRICULUM_DATA.characterMetrics.find(metrics => metrics.week === weekNumber)
}

export const getWeeklyGames = (weekNumber) => {
  return TCH_CURRICULUM_DATA.games.find(games => games.week === weekNumber)
}

export const getAllWeekThemes = () => {
  return TCH_CURRICULUM_DATA.weeks.map(week => ({
    week: week.week,
    theme: week.theme,
    concept: week.concept
  }))
}

export const getProgressMetrics = () => {
  return TCH_CURRICULUM_DATA.characterMetrics.map(metrics => ({
    week: metrics.week,
    title: metrics.title,
    description: metrics.description,
    parentMetric: metrics.parentMetric
  }))
}